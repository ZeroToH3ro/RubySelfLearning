#string
#method: upcase, downcase, length
phrase = "Free \nCode Camp"
#upcase
puts phrase.upcase();
#downcase
puts phrase.downcase();
#length
puts phrase.length();
#include ?
puts phrase.include? "Camp";
#access element
puts phrase[3];
#find index of character
puts phrase.index("amp");